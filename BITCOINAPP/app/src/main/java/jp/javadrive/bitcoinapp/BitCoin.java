package jp.javadrive.bitcoinapp;

public class BitCoin {

    private String time;
    private float bpi;
    private double size;

    public double getSize() {
        return size;
    }

    public void setSize(double size) {
        this.size = size;
    }



    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }


    public float getBpi() {
        return bpi;
    }

    public void setBpi(float bpi) {
        this.bpi = bpi;
    }




}

